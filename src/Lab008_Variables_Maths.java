public class Lab008_Variables_Maths {
    public static void main(String[] args) {
        int a=20;
        a=20+20;
//        'a' is assigned the value 20
//        'a' is reassigned to the result of 20 + 20, which is 40
//        Prints the current value of 'a', which is now 40
        System.out.println(a);
    }
}
